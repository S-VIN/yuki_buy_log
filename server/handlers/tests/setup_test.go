package handlers_test

import (
	"os"
	"testing"
)

// TestMain позволяет выполнить код перед запуском тестов
// Но init() в imported packages уже выполнен, поэтому мы используем
// переменную окружения, которая проверяется при импорте
func TestMain(m *testing.M) {
	// Примечание: переменная окружения должна быть установлена ДО запуска тестов
	// либо через командную строку: SKIP_DB_INIT=true go test ./...
	// либо в IDE настройках запуска тестов
	os.Exit(m.Run())
}

func init() {
	// Для IDE и локального запуска тестов
	// Примечание: это не сработает для уже импортированных пакетов
	// но поможет для последующих инициализаций
	os.Setenv("SKIP_DB_INIT", "true")
}
